            <img src="..//images/medical.png" alt="a blood drop icon" id="blood_drop">
        </div>
         <br>
        <p id="wbd_t">Whole blood donation is the most common type of blood donation. During this donation, you donate
            about a pint (about half a liter) of whole blood. The blood is then separated into its components — red
            cells, plasma and sometimes platelets.</p>
        <div id="wid_div">
            <h1 id="wid">Why it's done ?</h1>
        </div>
         <br>
         <br>
        <p id="wid_t">You agree to have blood drawn so that it can be given to someone who needs a blood transfusion.

            Millions of people need blood transfusions each year. Some may need blood during surgery. Others depend on
            it after an accident or because they have a disease that requires certain parts of blood. Blood donation
            makes all of this possible. There is no substitute for human blood — all transfusions use blood from a
            donor.</p>
        <div id="risk_div">
            <h1 id="risk">Risks</h1>
            <img src="..//images/warning.png" alt="a warning sign" id="risk_sign">
        </div>
         <br>
        <p id="risk_t">Blood donation is safe. New, sterile disposable equipment is used for each donor, so there's no
            risk of
            getting a bloodborne infection by donating blood.

            Most healthy adults can donate a pint (about half a liter) safely, without health risks. Within a few days
            of a blood donation, your body replaces the lost fluids. And after two weeks, your body replaces the lost
            red blood cells.</p>
        <div id="er_div">
            <h1 id="er">Eligibility requirements</h1>
        </div>
         <br>
         <br>
        <p id="er_t">To be eligible to donate whole blood, plasma or platelets, you must be:
            <br>
            <br>
            <br>
            1- In good health.
            <br>
            <br>
            2- At least 16 or 17 years old, depending on the law in your state.
            Some states allow legal minors to donate
            with parent permission. While there's no legal upper age limit, policies may vary between individual donor
            centers.
            <br>
            <br>
            3- At least 110 pounds (about 50 kilograms).
            <br>
            <br>
            4- Able to pass the physical and health-history assessments.
            Eligibility requirements differ slightly among different types of blood donation.
        </p>
        <div id="fam_div">
            <h1 id="fam">Food and medications</h1>
        </div>
         <br>
        <p id="fam_t">Before your blood donation:
            <br>
            <br>
            <br>
            1- Get plenty of sleep the night before you plan to donate.
            <br>
            <br>
            2- Eat a healthy meal before your donation. Avoid fatty foods, such as a hamburger, fries or ice cream.
            <br>
            <br>
            3- Drink plenty of water before the donation.
            <br>
            <br>
            4- Check to see if any medications you are taking or recently took would prevent you from donating. For
            example, if you are a platelet donor, you must not take aspirin for two days prior to donating. Talk to a
            member of your health care team before you stop taking any medications in order to donate blood.
        </p>
        <div id="wyce_div">
            <h1 id="wyce">What you can expect ? <br> Before the procedure</h1>
        </div>
        <br>
        <br>
        <p id="wyce_t">
            Before you can donate blood, you will be asked to fill out a confidential medical history. It includes
            questions about behaviors known to carry a higher risk of bloodborne infections — infections that are
            transmitted through blood.
            <br>
            <br>
            <br>
            Because of the risk of bloodborne infections, not everyone can donate blood. The following are groups that
            are not eligible to donate blood:
            <br>
            <br>
            <br>
            1- Anyone who has used injected drugs, steroids or another substance not prescribed by a health care
            provider
            in the past three months
            <br>
            <br>
            2- Anyone who has had a new sexual partner in the past three months and has had anal sex in the past three
            months
            <br>
            <br>
            3- Anyone who has had multiple sexual partners in the past three months and has had anal sex in the past
            three
            months
            <br>
            <br>
            4- Anyone who has a congenital coagulation factor deficiency
            <br>
            <br>
            5- Anyone who has had a positive test for HIV
            <br>
            <br>
            6- Anyone who has engaged in sex for money or drugs in the past three months
            <br>
            <br>
            7- Anyone who, in the past three months, has had close contact with — lived with or had sexual contact with
            — a
            person who has hepatitis B or hepatitis C
            <br>
            <br>
            8- Anyone who has had babesiosis, a rare and severe tick-borne disease, in the past two years
            <br>
            <br>
            <br>
            You will also have a brief physical exam. The exam includes checking your blood pressure, pulse and
            temperature. A small sample of blood is taken from a finger prick and is used to check the oxygen-carrying
            part of your blood (hemoglobin level). If your hemoglobin concentration is within a healthy range, and
            you've met all the other screening requirements, you can donate blood.
        </p>
        <div id="atp_div">
            <h1 id="atp">After the procedure</h1>
        </div>
        <br>
         <br>
        <p id="atp_t">After donating, you sit in an observation area, where you rest and eat a light snack. After 15
            minutes, you can leave. After your blood donation:
            <br>
            <br>
            <br>
            1- Drink extra fluids.
            <br>
            <br>
            2- Avoid strenuous physical activity or heavy lifting for about 24 hours.
            <br>
            <br>
            3- If you feel lightheaded, lie down with your feet up until the feeling passes.
            <br>
            <br>
            4- Keep your bandage on and dry for the next four hours.
            If you have bleeding after removing the bandage, put pressure on the site and raise your arm until the
            bleeding stops.
            <br>
            <br>
            5- If bruising occurs, apply a cold pack to the area periodically during the first 24 hours.
            <br>
            <br>
            6- Consider adding iron-rich foods to your diet to replace the iron lost with blood donation.
            <br>
            <br>
            <br>
            Contact the blood donor center or your health care provider if you:

            Forgot to report any important health information to the blood donation center.
            Have signs and symptoms of an illness, such as a fever, within several days after your blood donation.
            Are diagnosed with COVID-19 within 48 hours after donating blood.
